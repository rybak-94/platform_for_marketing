class SiteController extends Controller
{
    
    /**
     * @inheritdoc
     */

     public function beforeAction($action)
     {
         if (parent::beforeAction($action)) {
             if ((!\Yii::$app->user->id) && ($action->id != "login")) {
                 Yii::$app->response->redirect(array('site/login'));
             }
             return true;
         } else {
             return false;
         }
     }


    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['login','logout', 'delete'],
                'rules' => [
                    [
                        'allow' => true,
                        'actions' => ['login'],
                        'roles' => ['?'],
                    ],
                    [
                        'allow' => true,
                        'actions' => ['logout'],
                        'roles' => ['@'],
                    ],
                    [
                        'allow' => true,
                        'actions' => ['delete'],
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'delete' => ['post'],
                    'logout' => ['post'],
                ],
            ],
        ];
    }

